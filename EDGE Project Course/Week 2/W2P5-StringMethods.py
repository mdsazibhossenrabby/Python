

text="Data Since is amazing!"

#Make all character upper case
uppercase_text=text.upper()
print(f"Upper case Sentence : {uppercase_text}")

#Replace word
replaced_text=text.replace("amazing","challenging")
print(f"Replaced word Sentence : {replaced_text}")

#Make all character lower case
lowercase_text=text.lower()
print(f"Lower case Sentence : {lowercase_text}")