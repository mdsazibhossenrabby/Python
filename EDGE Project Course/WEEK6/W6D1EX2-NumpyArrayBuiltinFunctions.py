

import numpy as np

# Zero Array
zeroArr1=np.zeros((2,4))
print(f"2by4 Zero Array : \n {zeroArr1}")


zeroArr2=np.zeros((4,2))
print(f"4by2 Zero Array : \n {zeroArr2}")


# ones Array 

onesArray1=np.ones((2,4))
print(f"2by4 Ones Array : \n {onesArray1}")


onesArray2=np.ones((4,2))
print(f"4by2 Ones Array : \n {onesArray2}")