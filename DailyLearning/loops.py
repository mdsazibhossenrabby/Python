#While Loop
i=0
while(i<6):
    print(f"While loop - Hello this is {i}")
    i+=1

#for loop 1 , 0 to n-1
for i in range(6):
    print(f"for loop 1 - Hello this is {i}")

#for loop 2 , x to y-1
for i in range(20,26):
    print(f"for loop 2 - Hello this is {i}")
    
#for loop 3 , start end-1 step
for i in range(100,120,3):
    print(f"for loop 3 - Hello this is {i}")
    
#for Loop through string data    
string="MD.SAZIB"
for c in string:
    print(c)